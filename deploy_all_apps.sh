#!/bin/bash
# PRINTMAXX PWA Deployment Script
# Deploys all 6 PWA apps to production
# Run: bash deploy_all_apps.sh

set -e

BASE_DIR="/Users/macbookpro/Documents/p/PRINTMAXX_STARTER_KITttttt/ralph/loops/app_factory/output"
OUTPUT_FILE="/Users/macbookpro/Documents/p/PRINTMAXX_STARTER_KITttttt/OPS/DEPLOYMENT_URLS.md"
TIMESTAMP=$(date '+%Y-%m-%d %H:%M:%S')

# Check for vercel CLI
if ! command -v vercel &> /dev/null && ! npx vercel --version &> /dev/null 2>&1; then
    echo "Installing Vercel CLI..."
    npm install -g vercel
fi

# Initialize output file
cat > "$OUTPUT_FILE" << 'HEADER'
# PRINTMAXX PWA Deployment URLs

**Auto-generated by deploy_all_apps.sh**

---

## Live Production URLs

| App | URL | Status | Deployed |
|-----|-----|--------|----------|
HEADER

# Apps in priority order (Ramadan first - starts Feb 28!)
APPS=(
    "ramadan-tracker"
    "focuslock-web"
    "habitforge-web"
    "mealmaxx-web"
    "sleepmaxx-web"
    "walktounlock-web"
)

DEPLOY_METHOD=""
SUCCESS_COUNT=0
FAIL_COUNT=0

# Detect best deployment method
detect_method() {
    if command -v vercel &> /dev/null || npx vercel --version &> /dev/null 2>&1; then
        DEPLOY_METHOD="vercel"
        echo "Using Vercel for deployment"
    elif command -v surge &> /dev/null || npx surge --version &> /dev/null 2>&1; then
        DEPLOY_METHOD="surge"
        echo "Using Surge.sh for deployment"
    elif command -v netlify &> /dev/null || npx netlify --version &> /dev/null 2>&1; then
        DEPLOY_METHOD="netlify"
        echo "Using Netlify for deployment"
    else
        echo "No deployment tool found. Installing Vercel..."
        npm install -g vercel
        DEPLOY_METHOD="vercel"
    fi
}

deploy_vercel() {
    local app_name=$1
    local app_dir="$BASE_DIR/$app_name"

    echo ""
    echo "=========================================="
    echo "Deploying: $app_name (Vercel)"
    echo "=========================================="

    cd "$app_dir"

    # Deploy to production
    URL=$(npx vercel deploy --prod --yes 2>&1)

    if [ $? -eq 0 ] && [[ "$URL" == http* ]]; then
        echo "SUCCESS: $URL"
        echo "| $app_name | $URL | LIVE | $TIMESTAMP |" >> "$OUTPUT_FILE"
        SUCCESS_COUNT=$((SUCCESS_COUNT + 1))
        return 0
    else
        echo "Vercel failed for $app_name, trying Surge fallback..."
        return 1
    fi
}

deploy_surge() {
    local app_name=$1
    local app_dir="$BASE_DIR/$app_name"
    local domain="${app_name}.surge.sh"

    echo ""
    echo "=========================================="
    echo "Deploying: $app_name (Surge.sh)"
    echo "=========================================="

    cd "$app_dir"

    # Deploy to surge
    npx surge . "$domain" 2>&1

    if [ $? -eq 0 ]; then
        local URL="https://$domain"
        echo "SUCCESS: $URL"
        echo "| $app_name | $URL | LIVE | $TIMESTAMP |" >> "$OUTPUT_FILE"
        SUCCESS_COUNT=$((SUCCESS_COUNT + 1))
        return 0
    else
        echo "FAILED: $app_name on Surge"
        echo "| $app_name | FAILED | ERROR | $TIMESTAMP |" >> "$OUTPUT_FILE"
        FAIL_COUNT=$((FAIL_COUNT + 1))
        return 1
    fi
}

deploy_app() {
    local app_name=$1
    local app_dir="$BASE_DIR/$app_name"

    # Check app directory exists
    if [ ! -d "$app_dir" ]; then
        echo "SKIP: $app_dir does not exist"
        echo "| $app_name | N/A | NOT FOUND | $TIMESTAMP |" >> "$OUTPUT_FILE"
        return 1
    fi

    # Check index.html exists
    if [ ! -f "$app_dir/index.html" ]; then
        echo "SKIP: $app_dir/index.html does not exist"
        echo "| $app_name | N/A | NO INDEX.HTML | $TIMESTAMP |" >> "$OUTPUT_FILE"
        return 1
    fi

    # Try Vercel first, fall back to Surge
    if [ "$DEPLOY_METHOD" = "vercel" ]; then
        deploy_vercel "$app_name" || deploy_surge "$app_name"
    elif [ "$DEPLOY_METHOD" = "surge" ]; then
        deploy_surge "$app_name"
    else
        deploy_vercel "$app_name" || deploy_surge "$app_name"
    fi
}

# Main execution
echo "============================================"
echo "  PRINTMAXX PWA DEPLOYMENT - $(date)"
echo "============================================"
echo ""
echo "Apps to deploy: ${#APPS[@]}"
echo "Base directory: $BASE_DIR"
echo ""

detect_method

for app in "${APPS[@]}"; do
    deploy_app "$app"
done

# Add summary to output file
cat >> "$OUTPUT_FILE" << FOOTER

---

## Deployment Summary

- **Total apps:** ${#APPS[@]}
- **Successful:** $SUCCESS_COUNT
- **Failed:** $FAIL_COUNT
- **Method:** $DEPLOY_METHOD
- **Timestamp:** $TIMESTAMP

## Quick Links (copy for social/marketing)

Check the table above for live URLs.

## Notes

- All apps are single-file PWAs (index.html + manifest.json + sw.js)
- All apps work offline after first visit
- No backend/database required - all data stored in browser localStorage
- Ramadan Tracker (Hilal) is HIGHEST PRIORITY - Ramadan starts Feb 28, 2026
FOOTER

echo ""
echo "============================================"
echo "  DEPLOYMENT COMPLETE"
echo "  Success: $SUCCESS_COUNT / ${#APPS[@]}"
echo "  Failed: $FAIL_COUNT"
echo "  URLs saved to: $OUTPUT_FILE"
echo "============================================"

# Open the results file
open "$OUTPUT_FILE"
