# ─── STEP 3: ANALYZE + SCORE ──────────────────────────────────────────────────
def score_niche(niche):
    score = 0
    comm = niche.get("community_total", 0)
    if comm >= 5000000:
        score += 5
    elif comm >= 1000000:
        score += 4
    elif comm >= 500000:
        score += 3
    elif comm >= 100000:
        score += 2
    else:
        score += 1
    if not niche.get("streak_app_exists", True):
        score += 5
    if niche.get("verdict") == "BLUE_OCEAN":
        score += 3
    if len(niche.get("itunes_results", [])) >= 3:
        score += 2
    priority = "P0" if score >= 13 else ("P1" if score >= 10 else "P2")
    return score, priority

