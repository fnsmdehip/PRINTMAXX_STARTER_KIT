# ─── STEP 2: SCORE & ANALYZE ──────────────────────────────────────────────────
def score_niche(niche_data):
    """Score a niche on 5 dimensions (0-5 each, max 25)."""
    total_community = sum(niche_data["reddit_communities"].values())

    # 1. Community size (0-5)
    if total_community >= 30_000_000:
        community_score = 5
    elif total_community >= 15_000_000:
        community_score = 4
    elif total_community >= 5_000_000:
        community_score = 3
    elif total_community >= 1_000_000:
        community_score = 2
    else:
        community_score = 1

    # 2. Blue ocean (exact match apps found?)
    exact_matches = len(niche_data.get("exact_match_apps", []))
    ocean_score = 5 if exact_matches == 0 else max(0, 5 - exact_matches * 2)

    # 3. App store competition density (apps with high ratings = crowded)
    itunes = niche_data.get("itunes_results", [])
    high_rated_direct = sum(1 for a in itunes if a.get("rating", 0) >= 4.7 and a.get("rating_count", 0) > 50000)
    competition_score = max(0, 5 - high_rated_direct)

    # 4. Content richness (can we build 365 days of lessons?)
    gap_note = niche_data.get("gap_note", "")
    richness_score = 5 if len(gap_note) > 300 else 3 if len(gap_note) > 150 else 2

    # 5. Monetization signal (evidence of paid books/courses = WTP exists)
    wtp_keywords = ["sold", "million", "bestseller", "series", "$", "award", "copies"]
    wtp_hits = sum(1 for kw in wtp_keywords if kw.lower() in gap_note.lower())
    monetization_score = min(5, wtp_hits)

    total = community_score + ocean_score + competition_score + richness_score + monetization_score
    return {
        "total_community": total_community,
        "score": total,
        "breakdown": {
            "community": community_score,
            "ocean": ocean_score,
            "competition": competition_score,
            "content_richness": richness_score,
            "monetization_wtp": monetization_score,
        },
        "gap_note": gap_note,
    }

