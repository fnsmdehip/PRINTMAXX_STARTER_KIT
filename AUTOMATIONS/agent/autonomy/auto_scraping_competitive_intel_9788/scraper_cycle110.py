    max_ratings = max((r.get("rating_count", 0) for r in itunes), default=0)
    if max_ratings >= 1000000:
        score += 5
    elif max_ratings >= 200000:
        score += 4
    elif max_ratings >= 50000:
        score += 3
    elif max_ratings >= 10000:
        score += 2
    else:
        score += 1

    # Education content signals from notes
    for signal in ["P0", "DIRECT BUILD", "Score: 2"]:
        if signal in notes:
            score += 2
            break

    return min(score, 25)


