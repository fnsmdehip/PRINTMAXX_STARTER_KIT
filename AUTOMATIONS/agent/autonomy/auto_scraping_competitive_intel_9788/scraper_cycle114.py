# ─── STEP 2: CLEAN & SCORE ────────────────────────────────────────────────────
def score_niche(niche_data: dict) -> dict:
    community = niche_data.get("community_total", 0)
    verdict = niche_data.get("verdict", "")
    notes = niche_data.get("notes", "")

    # Score 1-25 based on community size + verdict + signal strength
    if community >= 40000000:
        base_score = 25
    elif community >= 25000000:
        base_score = 24
    elif community >= 15000000:
        base_score = 23
    elif community >= 8000000:
        base_score = 22
    elif community >= 4000000:
        base_score = 21
    else:
        base_score = 20

    # Boost for direct build signal
    if "DIRECT BUILD" in notes and verdict == "BLUE_OCEAN":
        base_score = min(25, base_score)

    priority = "P0" if base_score >= 23 else "P1"
    return {"score": base_score, "priority": priority}


