# ─── STEP 2: CLASSIFIER ───────────────────────────────────────────────────────
def classify_niche(niche_key, niche_data):
    """
    Improved classifier (C079+, fixed C098):
    - Trust explicit 'verdict' field if set in raw data
    - Fallback: streak_app_exists=False → BLUE_OCEAN if community meets threshold
    - CRITICAL FIX: Do NOT scan category descriptions for streak signals.
      The descriptions intentionally say "not a gamified daily X with streak XP" —
      that is negative context (competitor LACKS the feature), not positive presence.
      Only streak_app_exists=True or an app name explicitly being a streak app counts.
    """
    # Trust explicit verdict if researcher set it
    if niche_data.get("verdict"):
        return niche_data["verdict"]

    if niche_data.get("streak_app_exists") == True:
        return "OCCUPIED"

    # Check if any result is explicitly a streak-branded app (app NAME contains streak/habit)
    for r in niche_data.get("itunes_results", []):
        name = r.get("name", "").lower()
        if any(s in name for s in ["streak", "habit tracker", "daily habit"]):
            return "SOFT_COMPETITION"

    community = niche_data.get("community_total", 0)
    if community >= 200_000:
        return "BLUE_OCEAN"
    return "NICHE_BO"

