def load_existing_names(csv_path):
    """Load existing blue ocean names to prevent duplicates."""
    existing = set()
    if not os.path.exists(csv_path):
        return existing
    with open(csv_path, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for row in reader:
            if row.get("type", "").startswith("blue_ocean"):
                existing.add(row.get("name", "").lower().strip())
    return existing

