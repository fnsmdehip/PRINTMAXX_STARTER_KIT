# ─── STEP 2: SCORE NICHES ─────────────────────────────────────────────────────
def score_niche(niche_data):
    """Score blue ocean opportunity. Composite of: community size, app gap, market signals."""
    communities = niche_data.get("reddit_communities", {})
    total_community = sum(communities.values())
    exact_matches = len(niche_data.get("exact_match_apps", []))
    gap_note = niche_data.get("gap_note", "")

    # Community score: log-scale 0-10
    community_score = min(10, math.log10(max(total_community, 1)) * 1.4)

    # App gap score: 0 exact matches = 10, 1 = 7, 2 = 4, 3+ = 1
    gap_scores = {0: 10, 1: 7, 2: 4}
    app_gap_score = gap_scores.get(exact_matches, 1)

    # Market signal score: count commercial signals in gap_note
    signals = ["bestseller", "million", "NYT", "Nobel", "billion", "M+", "K+", "bestselling", "award"]
    signal_count = sum(1 for s in signals if s.lower() in gap_note.lower())
    market_signal_score = min(10, signal_count * 1.5)

    composite = (community_score * 0.4) + (app_gap_score * 0.35) + (market_signal_score * 0.25)
    return round(composite * 2.5)  # scale to ~25 max

