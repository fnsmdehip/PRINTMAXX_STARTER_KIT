# ─── STEP 2: SCORE AND CLASSIFY ───────────────────────────────────────────────
def score_niche(niche_key, data):
    community_total = sum(data["reddit_communities"].values())
    exact_match = len(data["exact_match_apps"])
    soft_competitors = len([a for a in data["itunes_results"] if "not" in a.get("note","").lower()])

    base = 20
    if community_total > 5000000: base += 2
    elif community_total > 1000000: base += 1
    if exact_match == 0: base += 2
    elif exact_match <= 1: base += 1
    if soft_competitors <= 1: base += 1
    return min(base, 25)

SCORED = {}
for niche, data in RAW["niches"].items():
    verdict = "BLUE_OCEAN" if len(data["exact_match_apps"]) == 0 else "SOFT_COMPETITION"
