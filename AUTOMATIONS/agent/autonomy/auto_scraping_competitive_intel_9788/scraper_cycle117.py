# ─── STEP 2: CLEAN DATA ───────────────────────────────────────────────────────
def clean_niche(name, data):
    community_total = sum(data["reddit_communities"].values())
    exact_competition = len(data.get("exact_match_apps", []))
    # Competition score: 10 = zero exact apps, 6 = 1-2 niche apps, 2 = 3+ direct apps
    if exact_competition == 0:
        comp_score = 10
    elif exact_competition <= 2:
        comp_score = 6
    else:
        comp_score = 2
    # Community score: 8 = >5M, 6 = 2-5M, 4 = <2M
    if community_total >= 5000000:
        comm_score = 8
    elif community_total >= 2000000:
        comm_score = 6
    else:
        comm_score = 4
    # WTP: psychology/education/self-improvement always 5
    wtp_score = 5
    score = comm_score + comp_score + wtp_score
    # Verdict
    if score >= 21:
        verdict = "BLUE_OCEAN"
    elif score >= 15:
        verdict = "SOFT_COMPETITION"
    else:
        verdict = "OCCUPIED"
    return {
        "community": community_total,
        "comm_score": comm_score,
        "comp_score": comp_score,
        "wtp_score": wtp_score,
        "score": score,
        "verdict": verdict
    }

