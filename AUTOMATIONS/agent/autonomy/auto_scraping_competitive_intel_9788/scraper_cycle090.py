# ─── STEP 2: CLEAN & VALIDATE ─────────────────────────────────────────────────
def clean(raw):
    cleaned = []
    seen = set()
    for niche_key, niche_data in raw["niches"].items():
        name = niche_key.replace("_", " ").title()
        community = niche_data["community_total"]
        verdict = niche_data["verdict"]
        notes = niche_data["notes"]
        key = niche_key.lower()
        if key not in seen:
            seen.add(key)
            cleaned.append({
                "niche": name,
                "community_total": community,
                "verdict": verdict,
                "streak_app_exists": niche_data["streak_app_exists"],
                "notes": notes,
                "cycle": raw["cycle"],
                "strategy": raw["strategy"],
                "timestamp": raw["timestamp"],
            })
    return cleaned

CLEANED = clean(RAW)

# ─── STEP 3: ANALYZE & SCORE ──────────────────────────────────────────────────
def analyze(cleaned):
    results = {"blue_oceans": [], "soft_competition": [], "occupied": []}
    for item in cleaned:
        if item["verdict"] == "BLUE_OCEAN":
            results["blue_oceans"].append(item)
        elif item["verdict"] == "SOFT_COMPETITION":
            results["soft_competition"].append(item)
        else:
            results["occupied"].append(item)
    results["blue_oceans"].sort(key=lambda x: x["community_total"], reverse=True)
    results["soft_competition"].sort(key=lambda x: x["community_total"], reverse=True)
    return results

