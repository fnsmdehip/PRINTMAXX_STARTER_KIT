# ─── STEP 2: CLEAN + DEDUPLICATE ─────────────────────────────────────────────
def clean_niches(raw_niches):
    seen = set()
    cleaned = []
    for niche_key, niche_data in raw_niches.items():
        label = niche_data.get("notes", "")[:60].lower()
        if label in seen:
            continue
        seen.add(label)
        cleaned.append({
            "key": niche_key,
            "community_total": niche_data["community_total"],
            "verdict": niche_data["verdict"],
            "notes": niche_data["notes"],
            "itunes_results": niche_data["itunes_results"],
            "reddit_communities": niche_data["reddit_communities"],
        })
    return cleaned

CLEANED = clean_niches(RAW["niches"])

# ─── STEP 3: ANALYZE + SCORE ──────────────────────────────────────────────────
def score_niche(niche):
    score = 0
    ct = niche["community_total"]
    if ct >= 5000000:
        score += 5
    elif ct >= 2000000:
        score += 4
    elif ct >= 1000000:
        score += 3
    elif ct >= 500000:
        score += 2
    else:
        score += 1
    if niche["verdict"] == "BLUE_OCEAN":
        score += 5
    if not any(r["category"].lower().startswith("streak") for r in niche["itunes_results"]):
        score += 3
    # bonus for proven book market signal
    if "sold" in niche["notes"] and ("M copies" in niche["notes"] or "M+" in niche["notes"]):
        score += 2
    priority = "P0" if score >= 13 else ("P1" if score >= 10 else "P2")
    return score, priority

