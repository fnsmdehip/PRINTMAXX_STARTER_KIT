# ─── STEP 2: SCORE ALL NICHES ─────────────────────────────────────────────────
def score_niche(niche_data: dict, niche_key: str) -> dict:
    """Score each niche on 5 dimensions: community size, book evidence, gap size, build ease, WTP."""
    community = niche_data["community_total"]
    notes = niche_data.get("notes", "")
    notes_lower = notes.lower()
    itunes = niche_data.get("itunes_results", [])
    verdict = niche_data.get("verdict", "UNKNOWN")

    # Community score (0-5)
    if community > 40000000:
        comm_score = 5
    elif community > 20000000:
        comm_score = 4
    elif community > 10000000:
        comm_score = 3
    elif community > 5000000:
        comm_score = 2
    else:
        comm_score = 1

    # Book evidence score (0-5): bestsellers / prizes = validated demand
    # Extract manual score embedded in notes if present (e.g. "Score: 25. P0.")
    import re as _re
    manual_match = _re.search(r'score:\s*(\d+)', notes_lower)
    if manual_match:
        manual_score = int(manual_match.group(1))
        # Derive priority directly from manual score embedded by analyst
        return {
            "community": community,
            "score": manual_score,
            "comm_score": comm_score,
            "book_score": 5,
            "gap_score": 5,
            "build_score": 4,
            "wtp_score": 5,
            "verdict": verdict,
            "priority": "P0" if manual_score >= 23 else ("P1" if manual_score >= 20 else "P2"),
        }

    # Fallback: compute score from signals
    book_signals = [
        "pulitzer", "nyt bestseller", "#1 nyt", "sold 1m", "sold 2m", "sold 3m", "sold 4m",
        "1m+ copies", "2m+ copies", "3m+ copies", "500k+", "500,000+", "million copies",
        "award", "bestseller", "oscar", "emmy", "bbc series", "prize"
    ]
    book_score = min(5, sum(1 for sig in book_signals if sig in notes_lower))

    # Gap score (0-5): no direct streak app = max gap
    direct_apps = sum(1 for app in itunes if "streak" in app.get("category", "").lower())
    gap_score = 5 if direct_apps == 0 else (3 if direct_apps <= 2 else 1)

    # Build ease (0-5): simpler content = easier build
    build_score = 4  # all finance history = text content, high build ease

    # WTP score (0-5): financial audience pays more
    wtp_keywords = [
        "entrepreneur", "investor", "finance", "business", "professional", "premium",
        "wtp", "pays", "high wtp", "willingness to pay", "high-wtp", "personal finance"
    ]
    wtp_score = min(5, sum(1 for kw in wtp_keywords if kw in notes_lower) + 3)

    total = comm_score + book_score + gap_score + build_score + wtp_score

    return {
        "community": community,
        "score": total,
        "comm_score": comm_score,
        "book_score": book_score,
        "gap_score": gap_score,
        "build_score": build_score,
        "wtp_score": wtp_score,
        "verdict": verdict,
        "priority": "P0" if total >= 23 else ("P1" if total >= 20 else "P2"),
    }


