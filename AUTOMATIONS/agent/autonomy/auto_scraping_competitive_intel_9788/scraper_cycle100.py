# ─── STEP 2: CLASSIFIER ───────────────────────────────────────────────────────
def classify(niche_data):
    """
    Improved classifier (C079+): verdict = BLUE_OCEAN only if streak_app_exists is False
    AND no iTunes result is a genuine streak/habit gamification app for this specific niche.
    """
    if niche_data.get("streak_app_exists"):
        return "OCCUPIED"
    ratings = [r.get("rating", 0) for r in niche_data.get("itunes_results", [])]
    counts = [r.get("rating_count", 0) for r in niche_data.get("itunes_results", [])]
    # Check if any result is a true streak/habit gamification app (not just a tool)
    streak_keywords = ["streak", "habit", "daily challenge", "xp", "gamif"]
    for r in niche_data.get("itunes_results", []):
        name_lower = r.get("name", "").lower()
        if any(k in name_lower for k in streak_keywords):
            return "OCCUPIED"
        # Category field describes WHY it's NOT a streak app — presence of tools is fine
    if niche_data.get("verdict") == "BLUE_OCEAN":
        return "BLUE_OCEAN"
    avg_rating = sum(ratings) / len(ratings) if ratings else 0
    if avg_rating > 4.5 and max(counts, default=0) > 500000:
        return "OCCUPIED"
    if avg_rating > 4.3:
        return "SOFT_COMPETITION"
    return "BLUE_OCEAN"

