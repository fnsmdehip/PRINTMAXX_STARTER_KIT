# ─── STEP 2: SCORE EACH NICHE ─────────────────────────────────────────────────
def score_niche(niche_data):
    reddit_communities = niche_data.get("reddit_communities", {})
    total_community = sum(reddit_communities.values())

    # 1. Community size score (0-5)
    if total_community >= 20000000:
        community_score = 5
    elif total_community >= 10000000:
        community_score = 4
    elif total_community >= 5000000:
        community_score = 3
    elif total_community >= 1000000:
        community_score = 2
    else:
        community_score = 1

    # 2. Blue ocean score (no exact-match apps = max score)
    exact_match_apps = niche_data.get("exact_match_apps", [])
    ocean_score = max(0, 5 - len(exact_match_apps))

    # 3. Competition score (fewer itunes results = less saturated)
    itunes_results = niche_data.get("itunes_results", [])
    if len(itunes_results) == 0:
        competition_score = 5
    elif len(itunes_results) == 1:
        competition_score = 4
    elif len(itunes_results) == 2:
        competition_score = 3
    elif len(itunes_results) <= 4:
        competition_score = 2
    else:
        competition_score = 1

    # 4. Content richness score
    gap_note = niche_data.get("gap_note", "")
    richness_score = 5 if len(gap_note) > 300 else 3 if len(gap_note) > 150 else 2

    # 5. Monetization signal (evidence of paid books/courses = WTP exists)
    wtp_keywords = ["sold", "million", "bestseller", "series", "$", "award", "copies", "Nobel", "Pulitzer"]
    wtp_hits = sum(1 for kw in wtp_keywords if kw.lower() in gap_note.lower())
    monetization_score = min(5, wtp_hits)

    total = community_score + ocean_score + competition_score + richness_score + monetization_score
    return {
        "total_community": total_community,
        "score": total,
        "breakdown": {
            "community": community_score,
            "ocean": ocean_score,
            "competition": competition_score,
            "content_richness": richness_score,
            "monetization_wtp": monetization_score,
        },
        "gap_note": gap_note,
    }

