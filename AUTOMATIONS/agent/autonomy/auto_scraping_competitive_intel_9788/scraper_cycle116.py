# ─── STEP 2: ANALYZE + SCORE ──────────────────────────────────────────────────
def score_niche(niche_data):
    community_total = niche_data["community_total"]
    verdict = niche_data["verdict"]

    # Community size score (0-10)
    if community_total >= 10_000_000:
        comm_score = 10
    elif community_total >= 5_000_000:
        comm_score = 8
    elif community_total >= 2_000_000:
        comm_score = 6
    elif community_total >= 1_000_000:
        comm_score = 5
    elif community_total >= 500_000:
        comm_score = 4
    else:
        comm_score = 3

    # Competition score (0-10)
    if verdict == "BLUE_OCEAN":
        comp_score = 10
    elif verdict == "SOFT_COMPETITION":
        comp_score = 6
    else:
        comp_score = 2

    # WTP score (0-5) based on community profile
    # Philosophy/science communities have educated users with higher WTP
    if "neuroscience" in niche_data["reddit_communities"] or \
       "philosophy" in niche_data["reddit_communities"]:
        wtp_score = 5
    else:
        wtp_score = 4

    total = comm_score + comp_score + wtp_score
    return {
        "community": community_total,
        "comm_score": comm_score,
        "comp_score": comp_score,
        "wtp_score": wtp_score,
        "score": total,
        "verdict": verdict,
    }


