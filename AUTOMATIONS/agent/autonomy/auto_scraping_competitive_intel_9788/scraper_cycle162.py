# ─── STEP 2: CLEAN + DEDUPLICATE ─────────────────────────────────────────────
def load_existing_names(csv_path):
    """Return set of normalized existing niche names to deduplicate."""
    existing = set()
    if not os.path.exists(csv_path):
        return existing
    with open(csv_path, newline="", encoding="utf-8") as f:
        for row in csv.DictReader(f):
            name = row.get("name", "").strip().lower().replace(" ", "_")
            existing.add(name)
    return existing

