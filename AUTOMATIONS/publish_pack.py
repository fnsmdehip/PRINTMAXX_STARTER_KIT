#!/usr/bin/env python3
"""
PRINTMAXX Publish Pack Generator
===============================

Goal:
  Convert APPROVED QA items into "ready to ship" publish packs.

This script is intentionally offline:
  - It does not post.
  - It prepares structured output for scheduling tools (Buffer CSV) and
    human review/execution.

Inputs:
  - OPS/CONTENT_QA_QUEUE/*.md (front matter contains status/source_file/platform)

Outputs:
  - output/publish_packs/manifest.json
  - output/publish_packs/items/<item_id>/* (source + QA + extracted caption)
  - output/publish_packs/buffer/buffer_import_<platform>.csv (Date, Time, Text)

Design:
  - Incremental: uses OPS/_state/publish_pack.json to avoid duplicates.
  - Conservative: only includes QA status that begins with "APPROVED".
"""

from __future__ import annotations

import argparse
import csv
import json
import re
from dataclasses import dataclass
from datetime import date, datetime, timedelta
from pathlib import Path
from typing import Dict, List, Tuple


BASE_DIR = Path(__file__).resolve().parent.parent
QA_DIR = BASE_DIR / "OPS" / "CONTENT_QA_QUEUE"
OUT_DIR = BASE_DIR / "output" / "publish_packs"
OUT_ITEMS_DIR = OUT_DIR / "items"
OUT_BUFFER_DIR = OUT_DIR / "buffer"

STATE_DIR = BASE_DIR / "OPS" / "_state"
STATE_FILE = STATE_DIR / "publish_pack.json"

MANIFEST_JSON = OUT_DIR / "manifest.json"

TZ_NOTE = "EST"  # simple label; scheduling tool handles local TZ config.


def now_iso() -> str:
    return datetime.now().strftime("%Y-%m-%d %H:%M:%S")


def _parse_front_matter(text: str) -> Tuple[Dict[str, str], str]:
    lines = text.splitlines(True)
    if not lines or lines[0].strip() != "---":
        return {}, text
    end_idx = None
    for i in range(1, len(lines)):
        if lines[i].strip() == "---":
            end_idx = i
            break
    if end_idx is None:
        return {}, text

    meta: Dict[str, str] = {}
    for raw in lines[1:end_idx]:
        s = raw.strip()
        if not s or s.startswith("#") or ":" not in s:
            continue
        k, v = s.split(":", 1)
        k = k.strip()
        v = v.strip()
        if k:
            meta[k] = v
    body = "".join(lines[end_idx + 1 :])
    return meta, body


def _load_state() -> Dict[str, object]:
    STATE_DIR.mkdir(parents=True, exist_ok=True)
    if not STATE_FILE.exists():
        return {"version": 1, "exported": {}}
    try:
        payload = json.loads(STATE_FILE.read_text(encoding="utf-8"))
        if isinstance(payload, dict):
            payload.setdefault("version", 1)
            payload.setdefault("exported", {})
            if not isinstance(payload["exported"], dict):
                payload["exported"] = {}
            return payload
    except Exception:
        pass
    return {"version": 1, "exported": {}}


def _save_state(state: Dict[str, object]) -> None:
    STATE_DIR.mkdir(parents=True, exist_ok=True)
    STATE_FILE.write_text(json.dumps(state, indent=2, sort_keys=True) + "\n", encoding="utf-8")


def _approved_status(s: str) -> bool:
    return s.strip().upper().startswith("APPROVED")


def _safe_id_from_path(path: Path) -> str:
    return path.stem.replace(" ", "_").replace("/", "_")


def _extract_caption_from_carousel_md(text: str) -> str:
    """
    Extract the caption section from carousel markdown generated by carousel_factory.
    Fallback to empty string.
    """
    lines = text.splitlines()
    start = None
    for i, line in enumerate(lines):
        if line.strip().lower() == "## caption (ig)":
            start = i + 1
            break
    if start is None:
        return ""

    out: List[str] = []
    for j in range(start, len(lines)):
        l = lines[j]
        if l.strip().startswith("## "):  # next section
            break
        out.append(l.rstrip())

    # Normalize spacing and trim.
    caption = "\n".join(out).strip()
    # Remove any trailing blank lines.
    caption = re.sub(r"\n{3,}", "\n\n", caption).strip()
    return caption


def _extract_persona_id_from_carousel_md(text: str) -> str:
    m = re.search(r"(?m)^-\\s*persona_id:\\s*`([^`]+)`\\s*$", text)
    if m:
        return m.group(1).strip()
    return ""


def _parse_suggested_time(s: str) -> str:
    """
    QA entries may use '11:00 EST'. Buffer CSV expects 'HH:MM' (tool handles TZ).
    """
    m = re.search(r"(\d{1,2}):(\d{2})", s)
    if not m:
        return "11:00"
    hh = int(m.group(1))
    mm = m.group(2)
    hh = max(0, min(23, hh))
    return f"{hh:02d}:{mm}"


def _next_publish_date() -> str:
    # Tomorrow by default.
    d = date.today() + timedelta(days=1)
    return d.isoformat()


def _write_buffer_row(platform: str, publish_date: str, publish_time: str, text: str) -> Path:
    OUT_BUFFER_DIR.mkdir(parents=True, exist_ok=True)
    out_csv = OUT_BUFFER_DIR / f"buffer_import_{platform}.csv"
    is_new = not out_csv.exists()
    with open(out_csv, "a", newline="", encoding="utf-8") as f:
        w = csv.writer(f)
        if is_new:
            w.writerow(["Date", "Time", "Text"])
        w.writerow([publish_date, publish_time, text])
    return out_csv


def _copy_text_file(dst: Path, content: str) -> None:
    dst.parent.mkdir(parents=True, exist_ok=True)
    dst.write_text(content, encoding="utf-8")


def tick(*, max_items: int = 20) -> int:
    QA_DIR.mkdir(parents=True, exist_ok=True)
    OUT_ITEMS_DIR.mkdir(parents=True, exist_ok=True)
    OUT_BUFFER_DIR.mkdir(parents=True, exist_ok=True)

    state = _load_state()
    exported = state.get("exported", {})
    if not isinstance(exported, dict):
        exported = {}
        state["exported"] = exported

    manifest: Dict[str, object] = {
        "generated_at": now_iso(),
        "items": [],
    }

    # Load existing manifest items so dashboard can show stable history.
    if MANIFEST_JSON.exists():
        try:
            prior = json.loads(MANIFEST_JSON.read_text(encoding="utf-8"))
            if isinstance(prior, dict) and isinstance(prior.get("items"), list):
                manifest["items"] = prior["items"]
        except Exception:
            pass

    added = 0
    # Slot collision avoidance: key -> count (used as 15-min offset).
    slot_counts: Dict[str, int] = {}
    qas = sorted(QA_DIR.glob("*.md"), key=lambda p: p.stat().st_mtime, reverse=True)

    for qa_path in qas:
        if added >= max_items:
            break

        rel_qa = str(qa_path.relative_to(BASE_DIR))
        if exported.get(rel_qa):
            continue

        try:
            qa_text = qa_path.read_text(encoding="utf-8", errors="replace")
        except Exception:
            continue

        meta, _body = _parse_front_matter(qa_text)
        status = (meta.get("status") or "").strip()
        if not _approved_status(status):
            continue

        source_rel = (meta.get("source_file") or "").strip()
        if not source_rel:
            continue
        source_path = (BASE_DIR / source_rel)
        if not source_path.exists():
            continue

        platform = (meta.get("platform") or "").strip().lower() or "unknown"
        ctype = (meta.get("content_type") or "").strip().lower() or "unknown"
        suggested_time = (meta.get("suggested_time") or "").strip()
        publish_time = _parse_suggested_time(suggested_time)
        publish_date = _next_publish_date()

        item_id = _safe_id_from_path(qa_path)
        item_dir = OUT_ITEMS_DIR / item_id
        item_dir.mkdir(parents=True, exist_ok=True)

        # Copy source and QA for traceability (no mutation).
        _copy_text_file(item_dir / "QA.md", qa_text)
        try:
            _copy_text_file(item_dir / "SOURCE.md", source_path.read_text(encoding="utf-8", errors="replace"))
        except Exception:
            _copy_text_file(item_dir / "SOURCE.md", f"[unreadable] {source_rel}\n")

        buffer_csv_path = None
        caption = ""
        preview_rel = ""
        persona_id = ""

        if ctype == "carousel" and platform == "instagram":
            try:
                src_text = source_path.read_text(encoding="utf-8", errors="replace")
            except Exception:
                src_text = ""
            caption = _extract_caption_from_carousel_md(src_text)
            persona_id = _extract_persona_id_from_carousel_md(src_text)
            if caption:
                _copy_text_file(item_dir / "caption.txt", caption + "\n")
                file_key = f"instagram_{persona_id}" if persona_id else "instagram"
                # Avoid collisions by offsetting per file_key/date/time.
                sk = f"{file_key}|{publish_date}|{publish_time}"
                n = slot_counts.get(sk, 0)
                slot_counts[sk] = n + 1
                if n:
                    try:
                        hh, mm = publish_time.split(":", 1)
                        dt = datetime.fromisoformat(f"{publish_date}T{hh}:{mm}:00")
                        dt2 = dt + timedelta(minutes=15 * n)
                        publish_time = dt2.strftime("%H:%M")
                    except Exception:
                        pass
                buffer_csv_path = _write_buffer_row(file_key, publish_date, publish_time, caption)

            slug = source_path.stem
            preview_path = BASE_DIR / "output" / "carousels" / slug / "index.html"
            if preview_path.exists():
                # Dashboard lives in output/dashboard; use relative link.
                preview_rel = str(preview_path.relative_to(BASE_DIR / "output"))
                _copy_text_file(item_dir / "preview_path.txt", str(preview_path) + "\n")

        # Record in manifest and mark exported.
        record = {
            "id": item_id,
            "qa_file": rel_qa,
            "source_file": source_rel,
            "platform": platform,
            "content_type": ctype,
            "persona_id": persona_id,
            "scheduled": {"date": publish_date, "time": publish_time, "tz_note": TZ_NOTE},
            "buffer_csv": str(buffer_csv_path) if buffer_csv_path else "",
            "preview_output_rel": preview_rel,
            "exported_at": now_iso(),
        }

        items = manifest.get("items", [])
        if isinstance(items, list):
            items.append(record)
            manifest["items"] = items

        exported[rel_qa] = {"at": now_iso(), "item_id": item_id, "platform": platform, "content_type": ctype}
        added += 1

    OUT_DIR.mkdir(parents=True, exist_ok=True)
    MANIFEST_JSON.write_text(json.dumps(manifest, indent=2, sort_keys=True) + "\n", encoding="utf-8")
    _save_state(state)

    print(f"publish_pack: added={added} total_items={len(manifest.get('items', [])) if isinstance(manifest.get('items'), list) else 0}")
    return 0


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--tick", action="store_true")
    ap.add_argument("--max", type=int, default=20)
    args = ap.parse_args()

    if args.tick:
        return tick(max_items=max(1, args.max))
    ap.print_help()
    return 2


if __name__ == "__main__":
    raise SystemExit(main())
