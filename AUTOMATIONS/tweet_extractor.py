#!/usr/bin/env python3
"""
Tweet Extractor — converts long-form posting queue content into tweet-length Buffer CSVs.

Scans CONTENT/social/posting_queue/ for files with >280 char content,
extracts tweet-sized chunks, and generates Buffer-ready CSVs.

Usage:
    python3 AUTOMATIONS/tweet_extractor.py                    # Generate today's Buffer CSV
    python3 AUTOMATIONS/tweet_extractor.py --count 40         # Generate 40 tweets
    python3 AUTOMATIONS/tweet_extractor.py --days 7           # Schedule across 7 days
    python3 AUTOMATIONS/tweet_extractor.py --per-day 8        # 8 tweets per day
"""

import csv
import os
import re
import sys
import datetime
from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parent.parent
QUEUE_DIR = PROJECT_ROOT / "CONTENT" / "social" / "posting_queue"
OUTPUT_DIR = PROJECT_ROOT / "CONTENT" / "social" / "printmaxxer"
TIMES = ["07:30", "09:00", "11:00", "13:00", "15:00", "17:00", "19:00", "21:00"]
MAX_TWEET_LEN = 275  # Leave room for safety


def extract_tweets_from_file(filepath: Path) -> list[str]:
    """Extract tweet-sized content from a single file."""
    try:
        content = filepath.read_text().strip()
    except Exception:
        return []

    if not content or len(content) < 20:
        return []

    # If already tweet-length, return as-is
    if len(content) <= MAX_TWEET_LEN:
        return [content]

    tweets = []

    # Strategy 1: Split by double newline (paragraphs)
    paragraphs = [p.strip() for p in content.split("\n\n") if p.strip()]
    for p in paragraphs:
        # Skip markdown headers, code blocks, metadata lines
        if p.startswith("#") or p.startswith("```") or p.startswith("---"):
            continue
        if p.startswith("**Tweet") or p.startswith("**Thread"):
            continue
        if "Status:" in p or "Generated by" in p or "Voice:" in p:
            continue

        # Clean markdown formatting
        cleaned = re.sub(r"\*\*([^*]+)\*\*", r"\1", p)  # Remove bold
        cleaned = re.sub(r"`([^`]+)`", r"\1", cleaned)  # Remove code ticks
        cleaned = cleaned.strip()

        if 20 < len(cleaned) <= MAX_TWEET_LEN:
            tweets.append(cleaned)

    # Strategy 2: If no paragraphs worked, try extracting quoted text
    quoted = re.findall(r'```\n?(.*?)\n?```', content, re.DOTALL)
    for q in quoted:
        q = q.strip()
        if 20 < len(q) <= MAX_TWEET_LEN:
            tweets.append(q)

    # Strategy 3: Split by numbered list items
    numbered = re.findall(r'(?:^|\n)\d+[/.)]\s*(.+?)(?=\n\d+[/.)]\s|\n\n|$)', content, re.DOTALL)
    for item in numbered:
        item = item.strip()
        if 20 < len(item) <= MAX_TWEET_LEN:
            tweets.append(item)

    return tweets


def deduplicate(tweets: list[str]) -> list[str]:
    """Remove duplicates and near-duplicates."""
    seen = set()
    unique = []
    for t in tweets:
        # Normalize for comparison
        key = re.sub(r'\s+', ' ', t.lower().strip())
        if key not in seen and len(key) > 20:
            seen.add(key)
            unique.append(t)
    return unique


def generate_buffer_csv(tweets: list[str], days: int = 5, per_day: int = 8) -> Path:
    """Write tweets to a Buffer-ready CSV."""
    today = datetime.date.today()
    date_str = today.strftime("%Y%m%d")
    output_path = OUTPUT_DIR / f"BUFFER_EXTRACTED_{date_str}.csv"

    with open(output_path, "w", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["Text", "Link", "Image", "Scheduled Date", "Scheduled Time", "Profile"])

        day_offset = 0
        time_idx = 0
        count = 0

        for tweet in tweets:
            if count >= days * per_day:
                break
            sched_date = today + datetime.timedelta(days=day_offset)
            writer.writerow([tweet, "", "", sched_date.strftime("%Y-%m-%d"), TIMES[time_idx], "printmaxxer Twitter"])
            count += 1
            time_idx += 1
            if time_idx >= len(TIMES[:per_day]):
                time_idx = 0
                day_offset += 1

    return output_path


def main():
    import argparse
    parser = argparse.ArgumentParser(description="Extract tweets from posting queue")
    parser.add_argument("--count", type=int, default=48, help="Max tweets to extract")
    parser.add_argument("--days", type=int, default=6, help="Days to schedule across")
    parser.add_argument("--per-day", type=int, default=8, help="Tweets per day")
    args = parser.parse_args()

    if not QUEUE_DIR.exists():
        print(f"Queue dir not found: {QUEUE_DIR}")
        sys.exit(1)

    all_tweets = []

    # Priority order: most recent first, twitter posts first
    files = sorted(QUEUE_DIR.glob("*.txt"), key=lambda f: f.stat().st_mtime, reverse=True)
    md_files = sorted(QUEUE_DIR.glob("*.md"), key=lambda f: f.stat().st_mtime, reverse=True)

    for filepath in files + md_files:
        extracted = extract_tweets_from_file(filepath)
        all_tweets.extend(extracted)
        if len(all_tweets) >= args.count * 3:  # Collect 3x for dedup headroom
            break

    unique_tweets = deduplicate(all_tweets)

    if not unique_tweets:
        print("No tweet-length content found in posting queue")
        sys.exit(0)

    output = generate_buffer_csv(unique_tweets[:args.count], args.days, args.per_day)
    print(f"Extracted {min(len(unique_tweets), args.count)} unique tweets")
    print(f"Total candidates scanned: {len(all_tweets)}")
    print(f"Output: {output}")
    print(f"Schedule: {args.per_day}/day for {args.days} days")


if __name__ == "__main__":
    main()
