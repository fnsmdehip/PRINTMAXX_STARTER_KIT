#!/usr/bin/env python3
"""Clean node_modules directories to reclaim space"""

import shutil
from pathlib import Path
from datetime import datetime

PROJECT_ROOT = Path(__file__).resolve().parent.parent
APP_FACTORY = PROJECT_ROOT / "MONEY_METHODS" / "APP_FACTORY" / "builds"

def clean_node_modules():
    """Delete all node_modules directories"""
    total_freed = 0
    count = 0

    for app_dir in APP_FACTORY.glob("*/"):
        node_modules = app_dir / "node_modules"
        if node_modules.exists():
            try:
                size_before = sum(f.stat().st_size for f in node_modules.rglob("*") if f.is_file()) / (1024*1024)
                shutil.rmtree(node_modules)
                count += 1
                total_freed += size_before
                print(f"✓ Deleted {app_dir.name}/node_modules ({size_before:.1f}MB)")
            except Exception as e:
                print(f"✗ Error deleting {app_dir.name}/node_modules: {e}")

    print(f"\n✓ Cleaned {count} directories, freed {total_freed:.1f}MB")
    return total_freed

if __name__ == "__main__":
    clean_node_modules()
